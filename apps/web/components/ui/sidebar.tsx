const SIDEBAR_COOKIE_NAME = "sidebar:state";
const SIDEBAR_COOKIE_MAX_AGE = 60 * 60 * 24 * 7;
const SIDEBAR_WIDTH = "16rem";
const SIDEBAR_WIDTH_MOBILE = "18rem";
const SIDEBAR_WIDTH_ICON = "3rem";
const SIDEBAR_KEYBOARD_SHORTCUT = "b";

const SidebarContext = React.createContext<{
  state: "expanded" | "collapsed";
  open: boolean;
  setOpen: (open: boolean) => void;
  openMobile: boolean;
  setOpenMobile: (open: boolean) => void;
  toggleSidebar: () => void;
} | null>(null);

function useSidebar() {
  const context = React.useContext(SidebarContext);
  if (!context) {
    throw new Error("useSidebar must be used within a SidebarProvider");
  }
  return context;
}

const SidebarProvider = React.memo(
  ({
    children,
    defaultOpen = true,
    ...props
  }: {
    children: React.ReactNode;
    defaultOpen?: boolean;
  }) => {
    const [openMobile, setOpenMobile] = React.useState(false);
    const [state, setState] = React.useState<"expanded" | "collapsed">(
      defaultOpen ? "expanded" : "collapsed"
    );
    const [open, setOpen] = React.useState(defaultOpen);

    // Toggle sidebar state
    const toggleSidebar = React.useCallback(() => {
      setState((prev) => {
        const newState = prev === "expanded" ? "collapsed" : "expanded";
        return newState;
      });
      setOpen((prev) => !prev);
    }, []);

    // Keyboard shortcut handler
    React.useEffect(() => {
      const handleKeyDown = (event: KeyboardEvent) => {
        if (
          event.key === SIDEBAR_KEYBOARD_SHORTCUT &&
          (event.metaKey || event.ctrlKey)
        ) {
          event.preventDefault();
          toggleSidebar();
        }
      };

      window.addEventListener("keydown", handleKeyDown);
      return () => window.removeEventListener("keydown", handleKeyDown);
    }, [toggleSidebar]);

    const value = React.useMemo(
      () => ({
        state,
        open,
        setOpen,
        openMobile,
        setOpenMobile,
        toggleSidebar,
      }),
      [state, open, openMobile, toggleSidebar]
    );

    return (
      <SidebarContext.Provider value={value} {...props}>
        {children}
      </SidebarContext.Provider>
    );
  }
);
SidebarProvider.displayName = "SidebarProvider";

const Sidebar = React.memo(
  ({
    side = "left",
    variant = "sidebar",
    collapsible = "icon",
    className,
    children,
    ...props
  }: {
    side?: "left" | "right";
    variant?: "sidebar" | "floating" | "inset";
    collapsible?: "icon" | "offcanvas" | "none";
    className?: string;
    children: React.ReactNode;
  }) => {
    const { state, openMobile, setOpenMobile } = useSidebar();
    const isMobile = typeof window !== "undefined" && window.innerWidth < 768;

    if (collapsible === "none") {
      return (
        <aside
          className={cn(
            "flex h-full w-[--sidebar-width] flex-col gap-2 bg-sidebar text-sidebar-foreground",
            className
          )}
          {...props}
        >
          {children}
        </aside>
      );
    }

    if (isMobile) {
      return (
        <Sheet open={openMobile} onOpenChange={setOpenMobile}>
          <SheetContent
            data-sidebar="sidebar"
            data-mobile="true"
            className="w-[--sidebar-width] bg-sidebar p-0 text-sidebar-foreground [&>button]:hidden"
            style={
              {
                "--sidebar-width": SIDEBAR_WIDTH_MOBILE,
              } as React.CSSProperties
            }
          >
            <div className="flex h-full w-full flex-col">{children}</div>
          </SheetContent>
        </Sheet>
      );
    }

    return (
      <aside
        className={cn(
          "group peer hidden md:block text-sidebar-foreground transition-[width] duration-200 ease-linear",
          "has-[[data-collapsible=icon]]:hover:bg-sidebar",
          state === "collapsed"
            ? "w-[--sidebar-width-icon] [&_[data-sidebar=menu-button]>span]:!-indent-[999em] [&_[data-sidebar=menu-button]>svg]:!translate-x-[-50%] [&_[data-sidebar=menu-button]>svg]:!opacity-0"
            : "w-[--sidebar-width]",
          className
        )}
        data-state={state}
        data-collapsible={state === "collapsed" ? "icon" : "offcanvas"}
        data-side={side}
        data-variant={variant}
        style={
          {
            "--sidebar-width": SIDEBAR_WIDTH,
            "--sidebar-width-icon": SIDEBAR_WIDTH_ICON,
          } as React.CSSProperties
        }
        {...props}
      >
        <div
          data-sidebar="sidebar"
          className="flex h-full w-full flex-col bg-sidebar group-data-[collapsible=icon]:bg-transparent"
        >
          {children}
        </div>
      </aside>
    );
  }
);
Sidebar.displayName = "Sidebar";

const SidebarTrigger = React.forwardRef<
  React.ElementRef<typeof Button>,
  React.ComponentProps<typeof Button>
>(({ className, onClick, ...props }, ref) => {
  const { toggleSidebar, setOpenMobile } = useSidebar();
  const isMobile = typeof window !== "undefined" && window.innerWidth < 768;

  return (
    <Button
      ref={ref}
      data-sidebar="trigger"
      variant="ghost"
      size="icon"
      className={cn("h-7 w-7", className)}
      onClick={(e) => {
        if (isMobile) {
          setOpenMobile(true);
        } else {
          toggleSidebar();
        }
        onClick?.(e);
      }}
      {...props}
    >
      <PanelLeft />
      <span className="sr-only">Toggle Sidebar</span>
    </Button>
  );
});
SidebarTrigger.displayName = "SidebarTrigger";

